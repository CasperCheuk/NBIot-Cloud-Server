//DMA串口初始化
void HAL_DMA_UsartInit(BC95_SP_ReceiveCallback rec_cb, uint32_t baud)
{
    baud = baud;
    bc95_rec_cb = rec_cb;
    HAL_UART_Receive_DMA(&huart1, usartDMA_Init(usart_dma_send, USART1), RECE_BUF_MAX_SIZE);
}


//DMA串口写数据
void HAL_USART_Write(uint8_t *buf, uint16_t len)
{
    //buf[len] = 0;
    printf("\r\n->:%s", buf);//uart2? 输出调试信息? 
    usartDMA_Write(buf, len);
    usart_dma_send(buf, len);
}

static void usart_dma_send(uint8_t *buf, uint16_t len)
{
    HAL_UART_Transmit_DMA(&huart1, buf, len);
    HAL_Delay(50);
    HAL_USART_Poll();
}

//DMA串口读数据
uint16_t HAL_USART_Read(uint8_t *buf, uint16_t len)
{
    return usartDMA_Read(buf, len);
}


//串口轮询事件
uint8_t HAL_USART_Poll(void)
{
    uint8_t evt = usartDMA_Poll_RecAvail();
    uint16_t len = 0;
    if (evt) {
        len = HAL_USART_RxBufLen();
        if (len) {
            len = usartDMA_Read(msgbuf, len);
            //msgbuf[len] = 0;
            printf("%s", msgbuf);
            if (bc95_rec_cb)
                bc95_rec_cb((char*)msgbuf, len);
        }
    }
    return evt;
}


//返回当前串口缓冲区有效的数据长度
uint16_t HAL_USART_RxBufLen(void)
{
    return usartDMA_Avail();
}

//关闭串口
void HAL_USART_Close(void)
{
    HAL_UART_MspDeInit(&huart1);
}
